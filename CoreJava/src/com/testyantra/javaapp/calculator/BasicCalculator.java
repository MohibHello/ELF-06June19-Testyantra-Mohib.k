package com.testyantra.javaapp.calculator;

public class BasicCalculator {

	public int add(int a, int b) {
		int res = a + b;
		return res;
	}

	public int sub(int a, int b) {
		int res = a - b;
		return res;
	}

	public int mul(int a, int b) {
		int res = a * b;
		return res;
	}

	public double div(int a, int b) {
		double res = a / b;
		return res;
	}
}
